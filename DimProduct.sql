      --,[DealerPrice]
      --,[Class]
      --,[Style]
      ,[ModelName] AS [Product Model Name]
      --,[LargePhoto]
      ,[EnglishDescription] AS [Product Description]
      --,[FrenchDescription]
      --,[ChineseDescription]
      --,[ArabicDescription]
      --,[HebrewDescription]
      --,[ThaiDescription]
      --,[GermanDescription]
      --,[JapaneseDescription]
      --,[TurkishDescription]
      --,[StartDate]
      --,[EndDate]
      ,ISNULL(p.Status,'OutDated') AS [Product Status] 
  FROM [AdventureWorksDW2019].[dbo].[DimProduct] p
  left join[AdventureWorksDW2019].[dbo].[DimProductSubcategory] sc on sc.ProductCategoryKey = p.ProductSubcategoryKey
  left join [AdventureWorksDW2019].[dbo].[DimProductCategory] pc on pc.ProductCategoryKey = sc.ProductCategoryKey
  order by P.ProductKey ASC
