	   [ProductKey]
      ,[ProductAlternateKey] AS ProductTitleCode
      --,[ProductSubcategoryKey]
      --,[WeightUnitMeasureCode]
      --,[SizeUnitMeasureCode] 
      ,[EnglishProductName] AS [Product Name]
	  ,pc.EnglishProductCategoryName AS [Product Category]
	  ,sc.EnglishProductSubcategoryName AS [Product Subcategory]
      --,[SpanishProductName]
